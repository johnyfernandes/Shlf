        Button {
            showLogSession = true
        } label: {
            HStack(spacing: 12) {
                // Pulsing green dot
                Circle()
                    .fill(Color.green)
                    .frame(width: 10, height: 10)
                    .overlay(
                        Circle()
                            .stroke(Color.green.opacity(0.3), lineWidth: 3)
                            .scaleEffect(1.5)
                    )
                    .modifier(PulseAnimation())

                if let activeSession = activeSessions.first {
                    VStack(alignment: .leading, spacing: 2) {
                        Text("Active Session")
                            .font(.headline)
                            .foregroundStyle(.primary)
                        Text("Started on \(activeSession.sourceDevice) • \(activeSession.pagesRead) pages")
                            .font(.caption)
                            .foregroundStyle(.secondary)
                    }
                }

                Spacer()

                Image(systemName: "chevron.right")
                    .font(.caption)
                    .foregroundStyle(.secondary)
            }
            .padding()
            .background(Color.green.opacity(0.1))
            .cornerRadius(12)
        }
        .buttonStyle(.plain)
    }

    private var statusSelector: some View {
        Menu {
            ForEach(ReadingStatus.allCases, id: \.self) { status in
                Button {
                    handleStatusChange(to: status)
                } label: {
                    HStack {
                        Image(systemName: status.icon)
                        Text(status.rawValue)
                        if book.readingStatus == status {
                            Spacer()
                            Image(systemName: "checkmark")
                        }
                    }
                }
            }
        } label: {
            HStack {
                Image(systemName: book.readingStatus.icon)
                    .font(.body)
                    .frame(width: 24)
                    .foregroundStyle(themeColor.color)

                Text(book.readingStatus.rawValue)
                    .font(Theme.Typography.body)
                    .foregroundStyle(Theme.Colors.text)

                Spacer()

                Image(systemName: "chevron.down")
                    .font(.caption.weight(.semibold))
                    .foregroundStyle(Theme.Colors.tertiaryText)
            }
            .padding(Theme.Spacing.md)
            .background(
                RoundedRectangle(cornerRadius: 12)
                    .fill(themeColor.color.opacity(0.08))
            )
            .overlay(
                RoundedRectangle(cornerRadius: 12)
