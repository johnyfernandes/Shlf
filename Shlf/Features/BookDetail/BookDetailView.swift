    private func estimateReadingTime(pages: Int) -> String {
        let minutesPerPage = 2.0
        let totalMinutes = Double(pages) * minutesPerPage
        let hours = Int(totalMinutes / 60)

        if hours < 1 {
            return "\(Int(totalMinutes)) min"
        } else if hours < 24 {
            return "~\(hours) hours"
        } else {
            let days = hours / 24
            return "~\(days) days"
        }
    }

    private func handleStatusChange(to status: ReadingStatus) {
        // If changing from Currently Reading with progress, show warning
        if book.readingStatus == .currentlyReading &&
           book.currentPage > 0 &&
           status != .currentlyReading {
            pendingStatus = status
            showStatusChangeAlert = true
        } else {
            updateReadingStatus(to: status)
        }
    }

    private func updateReadingStatus(to status: ReadingStatus) {
        let previousStatus = book.readingStatus

        // Save progress when leaving Currently Reading
        if previousStatus == .currentlyReading && status != .currentlyReading && book.currentPage > 0 {
            savedProgress = book.currentPage
        }

        book.readingStatus = status

        switch status {
        case .currentlyReading:
            if book.dateStarted == nil {
                book.dateStarted = Date()
            }
            // Offer to restore saved progress
            if let saved = savedProgress, saved > book.currentPage {
                book.currentPage = saved
            }
        case .finished:
            let previousPage = book.currentPage
            book.dateFinished = Date()
            if let totalPages = book.totalPages {
                book.currentPage = totalPages

                // Create session for the remaining pages if finishing from menu
                let pagesRead = totalPages - previousPage
                if pagesRead > 0 {
                    handleProgressSave(pagesRead: pagesRead)
                }
            }
            // Update goals when finishing a book
            if let profile = profile {
                let tracker = GoalTracker(modelContext: modelContext)
                tracker.updateGoals(for: profile)
            }
        case .wantToRead:
            // Reset progress if moving back to want to read
            if previousStatus != .wantToRead {
                book.currentPage = 0
                book.dateStarted = nil
                book.dateFinished = nil
            }
        case .didNotFinish:
            if book.dateFinished == nil {
                book.dateFinished = Date()
            }
        }

        // Sync to Watch whenever status changes
        Task {
            await WatchConnectivityManager.shared.syncBooksToWatch()
        }
    }

    private func markAsFinished() {
        updateReadingStatus(to: .finished)
    }

    private func handleProgressSave(pagesRead: Int) {
        // Create session with actual delta (can be negative for corrections)
        guard pagesRead != 0, let profile = profile else { return }

        let endPage = book.currentPage
        let startPage = endPage - pagesRead // Don't use max(), allow negative

        // Estimate reading time: industry standard is ~2 minutes per page
        let estimatedMinutes = abs(pagesRead) * 2

        let session = ReadingSession(
            startPage: startPage,
            endPage: endPage,
            durationMinutes: estimatedMinutes,
            xpEarned: 0, // Will be calculated below
            isAutoGenerated: true,
            book: book
        )

        // Calculate XP using the same engine as manual sessions
        let engine = GamificationEngine(modelContext: modelContext)
        session.xpEarned = engine.calculateXP(for: session)

        modelContext.insert(session)

        // Award XP/streak and achievements using authoritative engine
        engine.awardXP(session.xpEarned, to: profile)
        engine.updateStreak(for: profile, sessionDate: Date())
        engine.checkAchievements(for: profile)

        do {
            try modelContext.save()

            // Do ALL sync operations in background to avoid blocking UI
            let currentPage = book.currentPage
            Task.detached(priority: .userInitiated) {
                // Refresh Live Activity if it's running
                await ReadingSessionActivityManager.shared.updateActivity(
                    currentPage: currentPage,
                    xpEarned: session.xpEarned
                )

                // Keep Watch in sync with new session and stats
                await WatchConnectivityManager.shared.sendSessionToWatch(session)
                await WatchConnectivityManager.shared.sendProfileStatsToWatch(profile)

                // Export widget data
                await MainActor.run {
                    WidgetDataExporter.exportSnapshot(modelContext: modelContext)
                }
            }
        } catch {
            WatchConnectivityManager.logger.error("Failed to save quick session: \(error.localizedDescription)")
        }
    }

    private func deleteBook() {
        // Deleting a book cascade-deletes its sessions, so we need to recalculate stats
        modelContext.delete(book)

        // Recalculate stats after cascade deletion of sessions
        if let profile = profile {
            let engine = GamificationEngine(modelContext: modelContext)
            engine.recalculateStats(for: profile)
        }

        // Sync to Watch after deletion
        Task {
            await WatchConnectivityManager.shared.syncBooksToWatch()
            if let profile = profile {
                await WatchConnectivityManager.shared.sendProfileStatsToWatch(profile)
            }
        }

        // Update widget snapshot so deleted books disappear
        WidgetDataExporter.exportSnapshot(modelContext: modelContext)

        dismiss()
    }
}

struct ReadingSessionRow: View {
    let session: ReadingSession

    var body: some View {
        HStack(spacing: Theme.Spacing.md) {
            VStack(alignment: .leading, spacing: Theme.Spacing.xxs) {
                Text(session.startDate, style: .date)
                    .font(Theme.Typography.headline)
                    .foregroundStyle(Theme.Colors.text)

                Text(session.startDate, style: .time)
                    .font(Theme.Typography.caption)
                    .foregroundStyle(Theme.Colors.tertiaryText)
            }

            Spacer()

            VStack(alignment: .trailing, spacing: Theme.Spacing.xxs) {
                HStack(spacing: Theme.Spacing.xs) {
                    Image(systemName: "book.pages")
                        .font(.caption)

                    Text("\(session.pagesRead) pages")
                        .font(Theme.Typography.callout)
                }
                .foregroundStyle(Theme.Colors.text)

                HStack(spacing: Theme.Spacing.xs) {
                    Image(systemName: "clock")
                        .font(.caption)

                    Text("\(session.durationMinutes) min")
                        .font(Theme.Typography.caption)
                }
                .foregroundStyle(Theme.Colors.secondaryText)

                if session.xpEarned > 0 {
                    HStack(spacing: Theme.Spacing.xxs) {
                        Image(systemName: "star.fill")
                            .font(.caption2)

                        Text("+\(session.xpEarned) XP")
                            .font(Theme.Typography.caption2)
                    }
                    .foregroundStyle(Theme.Colors.xpGradient)
                }
            }
        }
        .padding(Theme.Spacing.md)
        .cardStyle()
    }
}

